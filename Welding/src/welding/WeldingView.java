    public void CalculateWelding(){
     try
        {
        try{
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver").newInstance();
            connection = DriverManager.getConnection(
                    "jdbc:odbc:welding");
           }
           catch (Exception e){
            System.err.println(" JDBC/ODBC  . "+e.getMessage());
           }
        
           Statement s = connection.createStatement();
                 
           if (newWeld.getUnits()){
               GenerateEnglishQueries();         
           }
           if (!newWeld.getUnits()){
               GenerateMetricQueries();             
           }

           GenerateGeneralQueries();
           
           SetIndicators(s);
           OutputResults(s);
   
        }
        catch (Exception e){
            System.err.println(": "+e.getMessage());
        }
    }

